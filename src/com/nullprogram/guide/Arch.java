package com.nullprogram.guide;

/**
 * Enumeration of pre-defined architectures.
 */
public enum Arch {
    /** 32-bit Linux. */
    LINUX_32,
    /** 64-bit Linux. */
    LINUX_64,
    /** 32-bit Microsoft Windows. */
    WINDOWS_32,
    /** 64-bit Microsoft Windows. */
    WINDOWS_64,
    /** 32-bit Mac OS X. */
    MAC_32,
    /** 64-bit Mac OS X. */
    MAC_64,
    /** Unknown architecture. */
    UNKNOWN;
}
